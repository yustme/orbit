def fetch_vectors(sc_id: int, start: str, stop: str, step: str = "30d") -> dict:
    params = {
        "format": "json",
        "COMMAND": str(sc_id),
        "OBJ_DATA": "NO",
        "MAKE_EPHEM": "YES",
        "EPHEM_TYPE": "VECTORS",
        "CENTER": "500@10",   # Sun
        "START_TIME": start,
        "STOP_TIME": stop,
        "STEP_SIZE": step,
        "VEC_TABLE": "1",     # position + velocity
        "OUT_UNITS": "AU-D",
        "CSV_FORMAT": "YES",
        "VEC_LABELS": "NO",
        "REF_PLANE": "ECLIPTIC",
        "REF_SYSTEM": "J2000",
    }
    r = requests.get(HORIZONS_URL, params=params, timeout=60)
    r.raise_for_status()
    return r.json()


def parse_points(raw: dict) -> list:
    """Parse CSV vector output into [[t, x, y, z], ...] (t = days since J2000, xyz in AU)."""
    text = raw.get("result", "")
    points = []
    active = False
    for line in text.splitlines():
        s = line.strip()
        if s == "$$SOE":
            active = True
            continue
        if s == "$$EOE":
            break
        if not active or not s:
            continue
        parts = [p.strip() for p in s.split(",")]
        if len(parts) < 5:
            continue
        try:
            jd = float(parts[0])
            x  = float(parts[2])
            y  = float(parts[3])
            z  = float(parts[4])
            t  = round(jd - J2000_JD, 1)
            points.append([t, round(x, 5), round(y, 5), round(z, 5)])
        except ValueError:
            continue
    return points


def main() -> None:
    results = {}
    today = "2026-04-22"

    for name, sc_id, launch, end, color, status in SPACECRAFT:
        stop = end or today
        print(f"  {name:20s} ({sc_id:5d})  {launch} → {stop} ... ", end="", flush=True)
        try:
            raw = fetch_vectors(sc_id, launch, stop)
            if "error" in raw:
                print(f"ERROR: {raw['error']}")
                continue
            pts = parse_points(raw)
            print(f"{len(pts)} pts")
            results[name] = {
                "color": color,
                "status": status,
                "points": pts,
            }
        except Exception as exc:
            print(f"FAILED: {exc}")
        time.sleep(0.5)   # be polite to the API

    js = (
        "// Auto-generated by scripts/fetch_trajectories.py\n"
        "// Source: NASA JPL Horizons  https://ssd.jpl.nasa.gov/horizons/\n"
        "// t = days since J2000 (2000-Jan-01 12:00 TT)\n"
        "// x,y,z = AU in ecliptic J2000 frame\n"
        "//   X → vernal equinox,  Y → 90° ahead in ecliptic,  Z → ecliptic north\n"
        "// Three.js mapping: H.x→T.x,  H.y→T.z,  H.z→T.y  (Y=up)\n\n"
        "const trajectoryData = " +
        json.dumps(results, separators=(",", ":")) +
        ";\n"
    )
    OUT_FILE.write_text(js, encoding="utf-8")
    size = OUT_FILE.stat().st_size
    print(f"\nWrote {OUT_FILE}")
    print(f"  {size // 1024} KB,  {len(results)} spacecraft")


if __name__ == "__main__":
    print("Fetching spacecraft trajectories from NASA JPL Horizons...\n")
    main()
