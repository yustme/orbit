#!/usr/bin/env python3
"""
Fetch spacecraft trajectory data from NASA JPL Horizons API.
Outputs ../trajectories_data.js for the 3D Solar System visualization.

Usage:
    pip install requests
    python3 fetch_trajectories.py
"""
